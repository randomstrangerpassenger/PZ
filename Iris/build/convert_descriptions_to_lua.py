"""
convert_descriptions_to_lua.py — JSON→Lua 변환 스크립트
======================================================
descriptions_by_fulltype.v2.4.json + usecases_by_fulltype.v2.4.json
→ IrisUseCaseDescriptions.lua facade + UseCaseDescriptions/ChunkNNN.lua
  + UseCaseDescriptions/RequirementsLookup.lua

운영 규칙 (동결):
  - lines/debug_lines 순서 그대로 복사 (재정렬 금지)
  - surface는 키("context_menu"/"recipe_ui"/"both")로 저장 (로컬 문자열 금지)
  - label_key는 use_case_id 그대로 (빌드에서 i18n 치환 금지)
  - 변환 후 구조 검증: fulltype 수 + lines/debug_lines 키 존재
"""
import re
import sys
from pathlib import Path

# ── Paths ──
SCRIPT_DIR = Path(__file__).parent
if str(SCRIPT_DIR) not in sys.path:
    sys.path.insert(0, str(SCRIPT_DIR))

IRIS_DIR = SCRIPT_DIR.parent
OUTPUT_DIR = IRIS_DIR / "output"

from tools.common.io import load_json
from tools.common.versions import BUILD_VERSION

DESCRIPTIONS_PATH = OUTPUT_DIR / f"descriptions_by_fulltype.{BUILD_VERSION}.json"
USECASES_PATH = OUTPUT_DIR / f"usecases_by_fulltype.{BUILD_VERSION}.json"
NAV_REGISTRY_PATH = OUTPUT_DIR / f"recipe_nav_registry.{BUILD_VERSION}.json"
RECIPE_REQ_INDEX_PATH = OUTPUT_DIR / f"recipe_requirements_index.{BUILD_VERSION}.json"
LUA_OUTPUT_PATH = (
    IRIS_DIR / "media" / "lua" / "client" / "Iris" / "Data"
    / "IrisUseCaseDescriptions.lua"
)
USECASE_CHUNK_DIR = LUA_OUTPUT_PATH.parent / "UseCaseDescriptions"
REQUIREMENTS_LOOKUP_PATH = USECASE_CHUNK_DIR / "RequirementsLookup.lua"
USECASE_CHUNK_SIZE = 200

# Surface label → key 역매핑 (render_line에서 사용한 라벨 → 원래 키)
SURFACE_LABEL_TO_KEY = {
    "우클릭": "context_menu",
    "레시피": "recipe_ui",
    "우클릭+레시피": "both",
}


def lua_str(s: str) -> str:
    """Python 문자열을 Lua 문자열 리터럴로 변환.
    Non-ASCII 문자는 UTF-8 바이트 이스케이프 (\\NNN) 으로 변환.
    """
    result = []
    for ch in s:
        if ord(ch) > 127:
            # Non-ASCII → UTF-8 byte escape sequences
            for b in ch.encode("utf-8"):
                result.append("\\" + str(b))
        elif ch == '\\':
            result.append('\\\\')
        elif ch == '"':
            result.append('\\"')
        else:
            result.append(ch)
    return '"' + ''.join(result) + '"'


def lua_nil_or_str(val) -> str:
    """None → nil, 문자열 → Lua 문자열."""
    if val is None or val == "none":
        return "nil"
    return lua_str(val)


def build_lua_check(rq: dict) -> str:
    """atom의 check 필드를 Lua 테이블 문자열로 변환. 없으면 빈 문자열."""
    check = rq.get("check")
    if not check:
        return ""
    ctype = check.get("type", "")
    if ctype == "perk":
        return (
            f", check = {{ type = \"perk\", "
            f"perk_id = {lua_str(check['perk_id'])}, "
            f"level = {check['level']} }}"
        )
    elif ctype == "near_item":
        return (
            f", check = {{ type = \"near_item\", "
            f"near_token = {lua_str(check['near_token'])} }}"
        )
    elif ctype == "flag":
        return (
            f", check = {{ type = \"flag\", "
            f"flag_id = {lua_str(check['flag_id'])}, "
            f"recipe_name = {lua_str(check['recipe_name'])} }}"
        )
    return ""

def convert_to_lua(
    descriptions_data: dict,
    nav_registry: dict | None = None,
    req_index: dict | None = None,
) -> tuple[str, list[tuple[int, str, int]], str | None, int, int, list[str]]:
    """
    descriptions_by_fulltype → Lua facade + chunk table 문자열 변환.
    Returns: (facade_content, chunks, requirements_lookup_content, fulltype_count, total_lines, nav_errors)
    """
    nav_errors = []
    nav_entries = nav_registry.get("entries", {}) if nav_registry else {}
    req_entries = req_index.get("entries", {}) if req_index else {}
    fulltypes = descriptions_data.get("fulltypes", {})

    chunks: list[tuple[int, str, int]] = []
    chunk_index = 1
    chunk_entry_count = 0

    def start_chunk(index: int) -> list[str]:
        return [
            "-- Iris UseCase Descriptions Data Chunk",
            f"-- Auto-generated by convert_descriptions_to_lua.py",
            f"-- Source: descriptions_by_fulltype.{BUILD_VERSION}.json",
            f"-- Chunk: {index:03d}",
            "",
            "local chunk = {}",
            "",
        ]

    def finish_chunk(chunk_parts: list[str], index: int, entry_count: int) -> None:
        if entry_count <= 0:
            return
        chunk_parts.append("return chunk")
        chunk_parts.append("")
        chunks.append((index, "\n".join(chunk_parts), entry_count))

    parts = start_chunk(chunk_index)

    total_lines = 0

    for ft in sorted(fulltypes.keys()):
        if chunk_entry_count >= USECASE_CHUNK_SIZE:
            finish_chunk(parts, chunk_index, chunk_entry_count)
            chunk_index += 1
            chunk_entry_count = 0
            parts = start_chunk(chunk_index)

        ft_data = fulltypes[ft]
        block = ft_data.get("use_case_block", {})
        items = block.get("items", [])
        debug_items = block.get("debug_items", [])
        req_block = ft_data.get("requirements_block", {})
        req_lines = req_block.get("lines", [])
        req_debug_lines = req_block.get("debug_lines", [])

        parts.append(f'chunk[{lua_str(ft)}] = {{')

        evidence_lines_lua = []
        exclusion_lines_lua = []

        for item in items:
            ucid = item["use_case_id"]
            kind = item.get("line_kind", "evidence")
            if ucid and ucid.startswith("uc.exclusion."):
                kind = "exclusion"
            
            label_key = lua_str(ucid)
            display = lua_str(item["display_text"])
            surface = lua_str(item["surface"])
            strength = lua_nil_or_str(item.get("strength"))
            uniqueness = lua_nil_or_str(item.get("uniqueness"))

            # recipe 필드 삽입
            nav_ref_str = ""
            if ucid.startswith("uc.recipe.") and nav_registry:
                nav_entry = nav_entries.get(ucid)
                if nav_entry is None:
                    nav_errors.append(f"{ucid}: not found in recipe_nav_registry")
                else:
                    # original_name + translated_name은 모든 recipe 라인에 삽입 (표시용)
                    orig_name = nav_entry.get("original_name")
                    tr_name = nav_entry.get("translated_name")
                    if orig_name:
                        nav_ref_str += f", recipe_original_name = {lua_str(orig_name)}"
                    if tr_name:
                        nav_ref_str += f", recipe_translated_name = {lua_str(tr_name)}"
                    # recipe_nav_ref는 eligible만 삽입 (이동 버튼용)
                    if nav_entry.get("nav_eligible"):
                        nav_ref_str += (
                            f", recipe_nav_ref = {{ "
                            f"original_name = {lua_str(nav_entry['original_name'])}, "
                            f"translated_name = {lua_str(tr_name) if tr_name else 'nil'}, "
                            f"category = {lua_nil_or_str(nav_entry.get('category'))} }}"
                        )

                    # recipe_requirements (비어 있으면 생략 — I2 패턴)
                    if req_index:
                        reqs = req_entries.get(ucid, [])
                        if reqs:
                            req_parts = []
                            for rq in reqs:
                                req_parts.append(
                                    f"{{ kind = {lua_str(rq['kind'])}, "
                                    f"display = {lua_str(rq['display'])}"
                                    f"{build_lua_check(rq)} }}"
                                )
                            nav_ref_str += (
                                f", recipe_requirements = {{ {', '.join(req_parts)} }}"
                            )

            line_str = (
                f"        {{ label_key = {label_key}, "
                f"display_text = {display}, "
                f"surface = {surface}, "
                f"strength = {strength}, "
                f"uniqueness = {uniqueness}, "
                f"line_kind = {lua_str(kind)}"
                f"{nav_ref_str} }},"
            )

            if kind == "exclusion":
                exclusion_lines_lua.append(line_str)
            else:
                evidence_lines_lua.append(line_str)
                total_lines += 1

        # lines (Evidence only)
        parts.append("    lines = {")
        parts.extend(evidence_lines_lua)
        parts.append("    },")

        # exclusion_lines (Classification properties)
        parts.append("    exclusion_lines = {")
        parts.extend(exclusion_lines_lua)
        parts.append("    },")

        # debug_lines
        parts.append("    debug_lines = {")
        for item in debug_items:
            ucid = item["use_case_id"]
            label_key = lua_str(ucid)
            display = lua_str(item["display_text"])
            surface = lua_str(item["surface"])
            strength = lua_nil_or_str(item.get("strength"))
            uniqueness = lua_nil_or_str(item.get("uniqueness"))

            parts.append(
                f"        {{ label_key = {label_key}, "
                f"display_text = {display}, "
                f"surface = {surface}, "
                f"strength = {strength}, "
                f"uniqueness = {uniqueness} }},"
            )
            total_lines += 1

        parts.append("    },")

        # requirements_block (keep 도구 정보)
        if req_lines or req_debug_lines:
            parts.append("    requirements = {")
            for rline in req_lines:
                # 형식: "- {key} ({role})"
                m = re.match(r"^- (\S+)\s+\((.+?)\)", rline)
                if m:
                    rkey, rrole = m.group(1), m.group(2)
                else:
                    rkey, rrole = rline, "keep"
                parts.append(
                    f"        {{ label_key = {lua_str(rkey)}, "
                    f"role = {lua_str(rrole)}, "
                    f'surface = "recipe_ui" }},'
                )
                total_lines += 1
            parts.append("    },")
            parts.append("    requirements_debug = {")
            for rline in req_debug_lines:
                # 형식: "- {key} ({role}) [{recipe_id}]"
                m = re.match(r"^- (\S+)\s+\((.+?)\)\s+\[(.+?)\]", rline)
                if m:
                    rkey, rrole, rid = m.group(1), m.group(2), m.group(3)
                else:
                    rkey, rrole, rid = rline, "keep", ""
                parts.append(
                    f"        {{ label_key = {lua_str(rkey)}, "
                    f"role = {lua_str(rrole)}, "
                    f"recipe_id = {lua_str(rid)}, "
                    f'surface = "recipe_ui" }},'
                )
                total_lines += 1
            parts.append("    },")

        # require_block (v2.5 신규 — perk/near_item/flag)
        rq_block = ft_data.get("require_block", {})
        rq_lines = rq_block.get("lines", [])
        rq_debug_lines = rq_block.get("debug_lines", [])
        if rq_lines or rq_debug_lines:
            parts.append("    require = {")
            for rline in rq_lines:
                # 형식: "- {display} ({kind})"
                m = re.match(r"^- (.+)\s+\((.+?)\)$", rline)
                if m:
                    display, kind = m.group(1), m.group(2)
                else:
                    display, kind = rline, "unknown"
                parts.append(
                    f"        {{ display = {lua_str(display)}, "
                    f"kind = {lua_str(kind)} }},"
                )
                total_lines += 1
            parts.append("    },")
            parts.append("    require_debug = {")
            for rline in rq_debug_lines:
                # 형식: "- {display} ({kind}) [{recipe_id}]"
                m = re.match(r"^- (.+)\s+\((.+?)\)\s+\[(.+?)\]$", rline)
                if m:
                    display, kind, rid = m.group(1), m.group(2), m.group(3)
                else:
                    display, kind, rid = rline, "unknown", ""
                parts.append(
                    f"        {{ display = {lua_str(display)}, "
                    f"kind = {lua_str(kind)}, "
                    f"recipe_id = {lua_str(rid)} }},"
                )
                total_lines += 1
            parts.append("    },")

        parts.append("}")
        parts.append("")
        chunk_entry_count += 1

    finish_chunk(parts, chunk_index, chunk_entry_count)

    facade_parts = []
    facade_parts.append("-- Iris UseCase Descriptions Data")
    facade_parts.append(f"-- Auto-generated by convert_descriptions_to_lua.py")
    facade_parts.append(f"-- Source: descriptions_by_fulltype.{BUILD_VERSION}.json")
    facade_parts.append(f"-- Total fulltypes: {len(fulltypes)}")
    facade_parts.append(f"-- Chunks: {len(chunks)} x <= {USECASE_CHUNK_SIZE}")
    facade_parts.append("")
    facade_parts.append("local IrisUseCaseDescriptions = {}")
    facade_parts.append("")
    facade_parts.append("local chunkModules = {")
    for index, _content, _entry_count in chunks:
        facade_parts.append(f'    "Iris/Data/UseCaseDescriptions/Chunk{index:03d}",')
    facade_parts.append("}")
    facade_parts.append("")
    facade_parts.append("for _, moduleName in ipairs(chunkModules) do")
    facade_parts.append("    local chunk = require(moduleName)")
    facade_parts.append("    for fullType, entry in pairs(chunk) do")
    facade_parts.append("        IrisUseCaseDescriptions[fullType] = entry")
    facade_parts.append("    end")
    facade_parts.append("end")
    facade_parts.append("")

    requirements_lookup_content = None
    if req_index:
        lookup_parts = []
        lookup_parts.append("-- Iris Recipe Requirements Lookup")
        lookup_parts.append(f"-- Auto-generated by convert_descriptions_to_lua.py")
        lookup_parts.append(f"-- Source: recipe_requirements_index.{BUILD_VERSION}.json")
        lookup_parts.append("-- Keyed by original_name (English recipe name)")
        lookup_parts.append("")
        lookup_parts.append("local IrisRecipeRequirementsLookup = {}")
        lookup_parts.append("")

        nav_reqs_written = 0
        for ucid, reqs in sorted(req_entries.items()):
            if not reqs:
                continue
            nav_entry = nav_entries.get(ucid)
            if not nav_entry:
                continue
            orig_name = nav_entry.get("original_name")
            if not orig_name:
                continue

            req_parts_list = []
            for rq in reqs:
                req_parts_list.append(
                    f"{{ kind = {lua_str(rq['kind'])}, "
                    f"display = {lua_str(rq['display'])}"
                    f"{build_lua_check(rq)} }}"
                )
            lookup_parts.append(
                f"IrisRecipeRequirementsLookup[{lua_str(orig_name)}] = "
                f"{{ {', '.join(req_parts_list)} }}"
            )
            nav_reqs_written += 1

        lookup_parts.append("")
        lookup_parts.append(f"-- Total lookup entries: {nav_reqs_written}")
        lookup_parts.append("return IrisRecipeRequirementsLookup")
        lookup_parts.append("")
        requirements_lookup_content = "\n".join(lookup_parts)

        facade_parts.append("-- Recipe Requirements Lookup (for runtime fallback)")
        facade_parts.append(
            'IrisUseCaseDescriptions._requirementsLookup = '
            'require("Iris/Data/UseCaseDescriptions/RequirementsLookup")'
        )
        facade_parts.append("")

    facade_parts.append("return IrisUseCaseDescriptions")
    facade_parts.append("")

    return (
        "\n".join(facade_parts),
        chunks,
        requirements_lookup_content,
        len(fulltypes),
        total_lines,
        nav_errors,
    )


def structural_validation(
    lua_content: str,
    chunk_contents: list[str],
    expected_count: int,
) -> list[str]:
    """
    변환 결과 Lua 문자열 구조 검증.
    - fulltype 수 일치
    - 모든 entry에 lines/debug_lines 키 존재
    """
    errors = []
    combined_chunks = "\n".join(chunk_contents)

    # Count fulltypes
    ft_matches = re.findall(
        r'chunk\["[^"]+"\]', combined_chunks
    )
    actual = len(ft_matches)
    if actual != expected_count:
        errors.append(
            f"Fulltype count mismatch: expected={expected_count}, actual={actual}"
        )

    # Check structure: every entry must have lines and debug_lines
    entries = re.findall(
        r'chunk\["([^"]+)"\]\s*=\s*\{', combined_chunks
    )
    for ft in entries:
        # Find the block for this fulltype
        pattern = re.escape(f'chunk["{ft}"]')
        match = re.search(pattern + r"\s*=\s*\{(.+?)\n\}", combined_chunks, re.DOTALL)
        if match:
            block_text = match.group(1)
            if "lines = {" not in block_text:
                errors.append(f"{ft}: missing 'lines' key")
            if "debug_lines = {" not in block_text:
                errors.append(f"{ft}: missing 'debug_lines' key")

    return errors


def main():
    print("=" * 60)
    print(f"  Convert Descriptions → Lua (BUILD_VERSION={BUILD_VERSION})")
    print("=" * 60)

    # Check prerequisites
    for path, label in [
        (DESCRIPTIONS_PATH, "descriptions_by_fulltype"),
        (USECASES_PATH, "usecases_by_fulltype"),
    ]:
        if not path.exists():
            print(f"\n  ❌ {label} not found: {path}")
            return 1

    print(f"  Loading: {DESCRIPTIONS_PATH.name}")
    descriptions = load_json(DESCRIPTIONS_PATH)

    # recipe_nav_registry 로드 (FAIL-LOUD: recipe 라인 있는데 없으면 실패)
    nav_registry = None
    has_recipe_lines = any(
        item.get("use_case_id", "").startswith("uc.recipe.")
        for ft_data in descriptions.get("fulltypes", {}).values()
        for item in ft_data.get("use_case_block", {}).get("items", [])
    )
    if not NAV_REGISTRY_PATH.exists():
        if has_recipe_lines:
            print(f"\n  ❌ recipe_nav_registry REQUIRED but not found: {NAV_REGISTRY_PATH}")
            return 1
    else:
        nav_registry = load_json(NAV_REGISTRY_PATH)
        print(f"  Loaded: {NAV_REGISTRY_PATH.name}")

    # recipe_requirements_index 로드 (FAIL-LOUD: 파일 부재 시)
    req_index = None
    if not RECIPE_REQ_INDEX_PATH.exists():
        print(f"\n  ❌ recipe_requirements_index REQUIRED but not found: {RECIPE_REQ_INDEX_PATH}")
        return 1
    req_index = load_json(RECIPE_REQ_INDEX_PATH)
    print(f"  Loaded: {RECIPE_REQ_INDEX_PATH.name}")

    print("  Converting to Lua...")
    (
        lua_content,
        chunks,
        requirements_lookup_content,
        ft_count,
        line_count,
        nav_errors,
    ) = convert_to_lua(descriptions, nav_registry, req_index)
    print(
        f"  ✅ Converted: {ft_count} fulltypes, {line_count} total lines, "
        f"{len(chunks)} chunks"
    )

    if nav_errors:
        print(f"  ❌ Nav registry errors ({len(nav_errors)}):")
        for e in nav_errors[:10]:
            print(f"     {e}")
        return 1

    # Structural validation
    print("  Running structural validation...")
    errors = structural_validation(
        lua_content,
        [content for _index, content, _entry_count in chunks],
        ft_count,
    )
    if errors:
        print(f"  ❌ Validation FAILED ({len(errors)} errors):")
        for e in errors:
            print(f"     {e}")
        return 1
    print("  ✅ Validation PASSED")

    # Write output
    LUA_OUTPUT_PATH.parent.mkdir(parents=True, exist_ok=True)
    USECASE_CHUNK_DIR.mkdir(parents=True, exist_ok=True)
    for old_chunk in USECASE_CHUNK_DIR.glob("Chunk*.lua"):
        old_chunk.unlink()
    for index, content, _entry_count in chunks:
        chunk_path = USECASE_CHUNK_DIR / f"Chunk{index:03d}.lua"
        with open(chunk_path, "w", encoding="utf-8", newline="\n") as f:
            f.write(content)
    if requirements_lookup_content:
        with open(REQUIREMENTS_LOOKUP_PATH, "w", encoding="utf-8", newline="\n") as f:
            f.write(requirements_lookup_content)
    elif REQUIREMENTS_LOOKUP_PATH.exists():
        REQUIREMENTS_LOOKUP_PATH.unlink()
    with open(LUA_OUTPUT_PATH, "w", encoding="utf-8", newline="\n") as f:
        f.write(lua_content)

    print(f"  ✅ Saved: {LUA_OUTPUT_PATH.relative_to(IRIS_DIR)}")
    print(f"  ✅ Saved chunks: {USECASE_CHUNK_DIR.relative_to(IRIS_DIR)} ({len(chunks)})")
    if requirements_lookup_content:
        print(f"  ✅ Saved lookup: {REQUIREMENTS_LOOKUP_PATH.relative_to(IRIS_DIR)}")
    print("=" * 60)
    return 0


if __name__ == "__main__":
    sys.exit(main())
