from __future__ import annotations

import argparse
import re
from pathlib import Path


REPO_ROOT = Path(__file__).resolve().parents[6]
IRIS_ROOT = REPO_ROOT / "Iris"


def lua_quote(value: str) -> str:
    return '"' + value.replace("\\", "\\\\").replace('"', '\\"') + '"'


def normalize_fulltype(value: str, module_name: str) -> str:
    value = value.strip()
    if "." in value:
        return value
    return f"{module_name}.{value}"


def parse_fixers(paths: list[Path]) -> set[str]:
    fixers: set[str] = set()
    current_module = "Base"

    for path in paths:
        for line in path.read_text(encoding="utf-8", errors="ignore").splitlines():
            module_match = re.match(r"\s*module\s+([A-Za-z0-9_]+)", line)
            if module_match:
                current_module = module_match.group(1)
                continue

            fixer_match = re.match(r"\s*Fixer\s*:\s*([^,]+)", line)
            if not fixer_match:
                continue

            first_segment = fixer_match.group(1).split(";")[0].strip()
            item_name = first_segment.split("=")[0].strip()
            if item_name:
                fixers.add(normalize_fulltype(item_name, current_module))

    return fixers


def render_lua(fixers: set[str]) -> str:
    lines = [
        "-- Iris Fixing Index Data",
        "-- Auto-generated by build_iris_fixing_index_data.py",
        "-- DO NOT EDIT MANUALLY",
        "",
        "local IrisFixingIndexData = {}",
        "",
        "IrisFixingIndexData.fixers = {",
    ]
    for fulltype in sorted(fixers):
        lines.append(f"    [{lua_quote(fulltype)}] = true,")
    lines.append("}")
    lines.append("")
    lines.append("return IrisFixingIndexData")
    lines.append("")
    return "\n".join(lines)


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Build IrisFixingIndexData.lua")
    parser.add_argument(
        "--input",
        action="append",
        type=Path,
        dest="inputs",
        default=None,
        help="Fixing script input. May be passed multiple times.",
    )
    parser.add_argument(
        "--output",
        type=Path,
        default=IRIS_ROOT / "media" / "lua" / "client" / "Iris" / "Data" / "IrisFixingIndexData.lua",
    )
    return parser.parse_args()


def main() -> int:
    args = parse_args()
    inputs = args.inputs or [
        REPO_ROOT / "scripts" / "fixing.txt",
        REPO_ROOT / "scripts" / "vehicles" / "vehiclesfixing.txt",
    ]
    fixers = parse_fixers(inputs)
    args.output.parent.mkdir(parents=True, exist_ok=True)
    args.output.write_text(render_lua(fixers), encoding="utf-8", newline="\n")
    print(f"wrote {args.output} fixers={len(fixers)}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
