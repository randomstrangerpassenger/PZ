from __future__ import annotations

import argparse
import re
from collections import defaultdict
from pathlib import Path


REPO_ROOT = Path(__file__).resolve().parents[6]
IRIS_ROOT = REPO_ROOT / "Iris"

ALLOWED_MOVEABLES_TAGS = {
    "Crowbar",
    "SharpKnife",
    "Hammer",
    "Screwdriver",
    "Saw",
    "Wrench",
}


def lua_quote(value: str) -> str:
    return '"' + value.replace("\\", "\\\\").replace('"', '\\"') + '"'


def normalize_fulltype(value: str, short_name_map: dict[str, str]) -> str:
    value = value.strip()
    if "." in value:
        return value
    return short_name_map.get(value, f"Base.{value}")


def parse_script_items(script_root: Path) -> tuple[dict[str, set[str]], dict[str, str]]:
    tags_by_fulltype: dict[str, set[str]] = defaultdict(set)
    short_name_map: dict[str, str] = {}
    current_module = "Base"
    current_item: str | None = None

    for path in sorted(script_root.rglob("*.txt")):
        for line in path.read_text(encoding="utf-8", errors="ignore").splitlines():
            module_match = re.match(r"\s*module\s+([A-Za-z0-9_]+)", line)
            if module_match:
                current_module = module_match.group(1)
                current_item = None
                continue

            item_match = re.match(r"\s*item\s+([A-Za-z0-9_]+)", line)
            if item_match:
                current_item = item_match.group(1)
                short_name_map[current_item] = f"{current_module}.{current_item}"
                continue

            if current_item is None:
                continue

            tags_match = re.match(r"\s*Tags\s*=\s*([^,]+)", line)
            if not tags_match:
                continue

            fulltype = f"{current_module}.{current_item}"
            for raw_tag in re.split(r"[;:\s]+", tags_match.group(1).strip()):
                tag = raw_tag.strip()
                if tag:
                    tags_by_fulltype[fulltype].add(tag)

    return tags_by_fulltype, short_name_map


def parse_item_type_to_tag(moveables_lua: Path) -> dict[str, str]:
    text = moveables_lua.read_text(encoding="utf-8")
    return dict(re.findall(r'\["([^"]+)"\]\s*=\s*"([^"]+)"', text))


def parse_tool_definitions(moveables_lua: Path) -> list[tuple[str, list[str]]]:
    definitions: list[tuple[str, list[str]]] = []
    pattern = re.compile(
        r'addToolDefinition\(\s*"([^"]+)"\s*,\s*\{([^}]*)\}',
        re.MULTILINE,
    )
    text = moveables_lua.read_text(encoding="utf-8")
    for tool_name, item_block in pattern.findall(text):
        items = re.findall(r'"([^"]+)"', item_block)
        definitions.append((tool_name, items))
    return definitions


def build_index(script_root: Path, moveables_lua: Path) -> dict[str, object]:
    tags_by_fulltype, short_name_map = parse_script_items(script_root)
    fulltypes_by_tag: dict[str, list[str]] = defaultdict(list)
    for fulltype, tags in tags_by_fulltype.items():
        for tag in tags:
            fulltypes_by_tag[tag].append(fulltype)
    for tag in list(fulltypes_by_tag):
        fulltypes_by_tag[tag] = sorted(set(fulltypes_by_tag[tag]))

    item_type_to_tag = parse_item_type_to_tag(moveables_lua)
    definitions = parse_tool_definitions(moveables_lua)

    registered: set[str] = set()
    tag_mapping: dict[str, str] = {}

    for fulltype, tags in tags_by_fulltype.items():
        for tag in sorted(tags):
            if tag in ALLOWED_MOVEABLES_TAGS:
                tag_mapping[fulltype] = tag
                registered.add(fulltype)

    for tool_name, raw_items in definitions:
        for raw_item in raw_items:
            source_tag = item_type_to_tag.get(raw_item)
            if raw_item.startswith("Tag."):
                source_tag = raw_item[4:]

            if source_tag:
                expanded = fulltypes_by_tag.get(source_tag, [])
                for fulltype in expanded:
                    registered.add(fulltype)
                    if source_tag in ALLOWED_MOVEABLES_TAGS:
                        tag_mapping[fulltype] = source_tag
                continue

            fulltype = normalize_fulltype(raw_item, short_name_map)
            registered.add(fulltype)

            if tool_name in ALLOWED_MOVEABLES_TAGS:
                tag_mapping[fulltype] = tool_name
            else:
                for tag in sorted(tags_by_fulltype.get(fulltype, set())):
                    if tag in ALLOWED_MOVEABLES_TAGS:
                        tag_mapping[fulltype] = tag
                        break

    return {
        "registered": sorted(registered),
        "tagMapping": {key: tag_mapping[key] for key in sorted(tag_mapping)},
        "toolDefinitionCount": len(definitions),
    }


def render_lua(data: dict[str, object]) -> str:
    lines = [
        "-- Iris Moveables Index Data",
        "-- Auto-generated by build_iris_moveables_index_data.py",
        "-- DO NOT EDIT MANUALLY",
        "",
        "local IrisMoveablesIndexData = {}",
        "",
        "IrisMoveablesIndexData.registered = {",
    ]
    for fulltype in data["registered"]:
        lines.append(f"    [{lua_quote(fulltype)}] = true,")
    lines.append("}")
    lines.append("")
    lines.append("IrisMoveablesIndexData.tagMapping = {")
    for fulltype, tag in data["tagMapping"].items():
        lines.append(f"    [{lua_quote(fulltype)}] = {lua_quote(tag)},")
    lines.append("}")
    lines.append("")
    lines.append("return IrisMoveablesIndexData")
    lines.append("")
    return "\n".join(lines)


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Build IrisMoveablesIndexData.lua")
    parser.add_argument("--script-root", type=Path, default=REPO_ROOT / "scripts")
    parser.add_argument(
        "--moveables-lua",
        type=Path,
        default=REPO_ROOT / "lua" / "client" / "Moveables" / "ISMoveableDefinitions.lua",
    )
    parser.add_argument(
        "--output",
        type=Path,
        default=IRIS_ROOT / "media" / "lua" / "client" / "Iris" / "Data" / "IrisMoveablesIndexData.lua",
    )
    return parser.parse_args()


def main() -> int:
    args = parse_args()
    data = build_index(args.script_root, args.moveables_lua)
    args.output.parent.mkdir(parents=True, exist_ok=True)
    args.output.write_text(render_lua(data), encoding="utf-8", newline="\n")
    print(
        "wrote "
        f"{args.output} "
        f"registered={len(data['registered'])} "
        f"tagMapping={len(data['tagMapping'])} "
        f"toolDefinitions={data['toolDefinitionCount']}"
    )
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
