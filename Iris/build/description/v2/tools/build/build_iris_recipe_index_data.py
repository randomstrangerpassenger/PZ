from __future__ import annotations

import argparse
import json
from collections import defaultdict
from pathlib import Path
from typing import Any


IRIS_ROOT = Path(__file__).resolve().parents[5]


def lua_quote(value: str) -> str:
    return (
        '"'
        + value.replace("\\", "\\\\")
        .replace('"', '\\"')
        .replace("\n", "\\n")
        .replace("\r", "\\r")
        + '"'
    )


def normalize_fulltype(value: str, default_module: str = "Base") -> str:
    value = value.strip()
    if "." in value:
        return value
    return f"{default_module}.{value}"


def load_json(path: Path) -> dict[str, Any]:
    return json.loads(path.read_text(encoding="utf-8"))


def build_index(recipe_index: dict[str, Any], dynamic_catalog: dict[str, Any]) -> dict[str, Any]:
    item_roles: dict[str, list[dict[str, str]]] = defaultdict(list)
    seen: dict[str, set[tuple[str, str, str]]] = defaultdict(set)

    recipes = recipe_index.get("recipes", {})
    for recipe_id in sorted(recipes):
        recipe = recipes[recipe_id]
        category = str(recipe.get("category") or "Unknown")
        recipe_name = str(recipe.get("recipe_name") or recipe_id)

        for role, field_name in (("input", "inputs"), ("keep", "keeps")):
            for raw_fulltype in recipe.get(field_name, []) or []:
                fulltype = normalize_fulltype(str(raw_fulltype))
                key = (role, category, recipe_name)
                if key in seen[fulltype]:
                    continue
                seen[fulltype].add(key)
                item_roles[fulltype].append(
                    {
                        "role": role,
                        "category": category,
                        "recipe": recipe_name,
                    }
                )

    get_item_types: dict[str, list[str]] = {}
    groups = dynamic_catalog.get("groups", {})
    can_opener = groups.get("CanOpener")
    if can_opener and can_opener.get("status") == "resolved":
        get_item_types["CanOpener"] = sorted(
            normalize_fulltype(str(item))
            for item in can_opener.get("matched_fulltypes", [])
        )

    return {
        "version": str(recipe_index.get("version") or "unknown"),
        "itemRoles": {key: item_roles[key] for key in sorted(item_roles)},
        "getItemTypes": get_item_types,
    }


def render_lua(data: dict[str, Any]) -> str:
    lines: list[str] = [
        "-- Iris Recipe Index Data",
        "-- Auto-generated by build_iris_recipe_index_data.py",
        "-- DO NOT EDIT MANUALLY",
        "",
        "local IrisRecipeIndexData = {}",
        "",
        f"IrisRecipeIndexData.version = {lua_quote(data['version'])}",
        "IrisRecipeIndexData.itemRoles = {",
    ]

    for fulltype, roles in data["itemRoles"].items():
        lines.append(f"    [{lua_quote(fulltype)}] = {{")
        for entry in roles:
            lines.append(
                "        { role = "
                + lua_quote(entry["role"])
                + ", category = "
                + lua_quote(entry["category"])
                + ", recipe = "
                + lua_quote(entry["recipe"])
                + " },"
            )
        lines.append("    },")
    lines.append("}")
    lines.append("")
    lines.append("IrisRecipeIndexData.getItemTypes = {")
    for group_name, items in sorted(data["getItemTypes"].items()):
        lines.append(f"    [{lua_quote(group_name)}] = {{")
        for item in items:
            lines.append(f"        {lua_quote(item)},")
        lines.append("    },")
    lines.append("}")
    lines.append("")
    lines.append("return IrisRecipeIndexData")
    lines.append("")
    return "\n".join(lines)


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Build IrisRecipeIndexData.lua")
    parser.add_argument(
        "--recipe-index",
        type=Path,
        default=IRIS_ROOT / "output" / "recipe_index.v2.4.json",
    )
    parser.add_argument(
        "--dynamic-catalog",
        type=Path,
        default=IRIS_ROOT / "output" / "dynamic_expr_catalog.v2.4.json",
    )
    parser.add_argument(
        "--output",
        type=Path,
        default=IRIS_ROOT / "media" / "lua" / "client" / "Iris" / "Data" / "IrisRecipeIndexData.lua",
    )
    return parser.parse_args()


def main() -> int:
    args = parse_args()
    data = build_index(load_json(args.recipe_index), load_json(args.dynamic_catalog))
    args.output.parent.mkdir(parents=True, exist_ok=True)
    args.output.write_text(render_lua(data), encoding="utf-8", newline="\n")
    print(
        "wrote "
        f"{args.output} "
        f"items={len(data['itemRoles'])} "
        f"getItemTypes={len(data['getItemTypes'])}"
    )
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
