from __future__ import annotations

import re
from collections import OrderedDict
from pathlib import Path


IRIS_ROOT = Path(__file__).resolve().parents[3]
TRANSLATE_ROOT = IRIS_ROOT / "media" / "lua" / "shared" / "translate"
OUTPUT_PATH = IRIS_ROOT / "media" / "lua" / "client" / "Iris" / "Data" / "IrisTranslationData.lua"

LANGUAGE_FILES = {
    "EN": TRANSLATE_ROOT / "en" / "Iris_en.txt",
    "KO": TRANSLATE_ROOT / "ko" / "Iris_ko.txt",
}

ASSIGNMENT_RE = re.compile(r"\b(Iris_[A-Za-z0-9_]+)\s*=\s*\"((?:\\.|[^\"\\])*)\"")


def decode_lua_string(value: str) -> str:
    result = bytearray()
    i = 0
    while i < len(value):
        ch = value[i]
        if ch == "\\" and i + 1 < len(value):
            if i + 3 < len(value) and value[i + 1 : i + 4].isdigit():
                result.append(int(value[i + 1 : i + 4], 10))
                i += 4
                continue
            escape = value[i + 1]
            mapped = {
                "n": b"\n",
                "r": b"\r",
                "t": b"\t",
                '"': b'"',
                "\\": b"\\",
            }.get(escape)
            result.extend(mapped or escape.encode("utf-8"))
            i += 2
            continue
        result.extend(ch.encode("utf-8"))
        i += 1
    return result.decode("utf-8")


def parse_translation_file(path: Path) -> OrderedDict[str, str]:
    text = path.read_text(encoding="utf-8")
    entries: OrderedDict[str, str] = OrderedDict()
    for key, value in ASSIGNMENT_RE.findall(text):
        if key in entries:
            raise ValueError(f"duplicate translation key in {path}: {key}")
        entries[key] = decode_lua_string(value)
    return entries


def lua_escape(value: str) -> str:
    parts: list[str] = []
    for byte in value.encode("utf-8"):
        ch = chr(byte)
        if ch == "\\":
            parts.append("\\\\")
        elif ch == '"':
            parts.append('\\"')
        elif byte == 10:
            parts.append("\\n")
        elif byte == 13:
            parts.append("\\r")
        elif byte == 9:
            parts.append("\\t")
        elif 32 <= byte <= 126:
            parts.append(ch)
        else:
            parts.append(f"\\{byte:03d}")
    return "".join(parts)


def validate_key_parity(translations: dict[str, OrderedDict[str, str]]) -> list[str]:
    en_keys = list(translations["EN"].keys())
    en_key_set = set(en_keys)
    for lang, entries in translations.items():
        keys = set(entries.keys())
        missing = sorted(en_key_set - keys)
        extra = sorted(keys - en_key_set)
        if missing or extra:
            details = []
            if missing:
                details.append(f"missing={missing}")
            if extra:
                details.append(f"extra={extra}")
            raise ValueError(f"translation key mismatch for {lang}: " + "; ".join(details))
    return en_keys


def render_lua(translations: dict[str, OrderedDict[str, str]], key_order: list[str]) -> str:
    lines = [
        "-- Generated by Iris/build/tools/pipeline/build_iris_translation_data.py.",
        "-- Source of truth: Iris/media/lua/shared/translate/*/Iris_*.txt",
        "",
        "local data = {",
    ]
    for lang in ("EN", "KO"):
        lines.append(f"    {lang} = {{")
        entries = translations[lang]
        for key in key_order:
            lines.append(f'        {key} = "{lua_escape(entries[key])}",')
        lines.append("    },")
    lines.append("}")
    lines.append("")
    lines.append("return data")
    lines.append("")
    return "\n".join(lines)


def main() -> int:
    translations = {
        lang: parse_translation_file(path)
        for lang, path in LANGUAGE_FILES.items()
    }
    key_order = validate_key_parity(translations)
    OUTPUT_PATH.parent.mkdir(parents=True, exist_ok=True)
    OUTPUT_PATH.write_text(render_lua(translations, key_order), encoding="utf-8", newline="\n")
    print(f"wrote {OUTPUT_PATH} ({len(key_order)} keys per language)")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
