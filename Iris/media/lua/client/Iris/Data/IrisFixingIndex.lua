--[[
    IrisFixingIndex.lua - precompiled Fixing index wrapper

    Runtime code must not call ScriptManager fixing scanners. The data table is
    generated by Iris/build/description/v2/tools/build/build_iris_fixing_index_data.py.
]]

local IrisFixingIndex = {}

local safeRequire = require("Iris/Util/IrisRequire").safeRequire

local ok, data = safeRequire("Iris/Data/IrisFixingIndexData")
if not ok or not data then
    data = { fixers = {} }
end

IrisFixingIndex._fixers = data.fixers or {}
IrisFixingIndex._built = true
IrisFixingIndex.build_deprecated = true

--- @deprecated Precompiled data is loaded when this module is required.
function IrisFixingIndex.build()
    return true
end

--- 아이템이 Fixer 역할인지 확인
--- @param fullType string
--- @return boolean
function IrisFixingIndex.isFixer(fullType)
    return IrisFixingIndex._fixers[fullType] == true
end

--- Fixer 역할이 특정 role과 일치하는지
--- @param fullType string
--- @param role string
--- @return boolean
function IrisFixingIndex.roleEq(fullType, role)
    if role == "Fixer" then
        return IrisFixingIndex.isFixer(fullType)
    end
    return false
end

return IrisFixingIndex
