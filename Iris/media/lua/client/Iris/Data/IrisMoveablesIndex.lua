--[[
    IrisMoveablesIndex.lua - precompiled Moveables index wrapper

    Runtime code must not scan vanilla moveable definitions. The data table is
    generated by
    Iris/build/description/v2/tools/build/build_iris_moveables_index_data.py.
]]

local IrisMoveablesIndex = {}

local Array = require("Iris/Util/Array")
local safeRequire = require("Iris/Util/IrisRequire").safeRequire

local ok, data = safeRequire("Iris/Data/IrisMoveablesIndexData")
if not ok or not data then
    data = { registered = {}, tagMapping = {} }
end

IrisMoveablesIndex._registered = data.registered or {}
IrisMoveablesIndex._tagMapping = data.tagMapping or {}
IrisMoveablesIndex._built = true
IrisMoveablesIndex.build_deprecated = true

--- @deprecated Precompiled data is loaded when this module is required.
function IrisMoveablesIndex.build()
    return true
end

--- 아이템 ID가 Moveables에 등록되었는지 확인
--- @param fullType string
--- @return boolean
function IrisMoveablesIndex.isItemIdRegistered(fullType)
    return IrisMoveablesIndex._registered[fullType] == true
end

--- 아이템의 MoveablesTag 조회
--- @param fullType string
--- @return string|nil
function IrisMoveablesIndex.getMoveablesTag(fullType)
    return IrisMoveablesIndex._tagMapping[fullType]
end

--- 아이템이 허용된 MoveablesTag 중 하나에 매칭되는지
--- @param fullType string
--- @param allowedTags table
--- @return boolean
function IrisMoveablesIndex.tagIn(fullType, allowedTags)
    local tag = IrisMoveablesIndex._tagMapping[fullType]
    if not tag then
        return false
    end

    return Array.contains(allowedTags, tag)
end

return IrisMoveablesIndex
