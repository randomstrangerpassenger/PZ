--- 템플릿 가져오기
--- @param subcat_id string (예: "Tool.1-A")
--- @return table|nil template {header, lines}
function IrisDescTemplates.getTemplate(subcat_id)
    local template = templates[subcat_id]
    if template then
        print("[Templates.getTemplate] subcat_id = '" .. tostring(subcat_id) .. "'")
        print("[Templates.getTemplate] FOUND - header = '" .. template.header .. "'")
        print("[Templates.getTemplate]   lines count = " .. #template.lines)
        for i, line in ipairs(template.lines) do
            print("[Templates.getTemplate]   line[" .. i .. "] = '" .. line:sub(1, 50) .. "...'")
        end
    else
        print("[Templates.getTemplate] subcat_id = '" .. tostring(subcat_id) .. "'")
        print("[Templates.getTemplate] NOT FOUND")
    end
    return template
end

--- 사용 가능한 모든 템플릿 ID 반환
--- @return table<string> ids
function IrisDescTemplates.getAllIds()
    local ids = {}
    for id, _ in pairs(templates) do
        table.insert(ids, id)
    end
    return ids
end

return IrisDescTemplates
